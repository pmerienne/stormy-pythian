package stormy.pythian.core.configuration;

import stormy.pythian.core.description.PropertyDescription;

public class PropertyConfiguration implements Validable {

    private PropertyDescription description;

    private String name;
    private Object value;

    public PropertyConfiguration() {
    }

    public PropertyConfiguration(String name, Object value) {
        this.name = name;
        this.value = value;
    }

    public PropertyConfiguration(String name, Object value, PropertyDescription description) {
        this.description = description;
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Object getValue() {
        return value;
    }

    public void setValue(Object value) {
        this.value = value;
    }

    public PropertyDescription getDescription() {
        return description;
    }

    public void setDescription(PropertyDescription description) {
        this.description = description;
    }

    @Override
    public ValidationResult validate() {
        ValidationResult result = new ValidationResult(name);

        if (description.isMandatory() && value == null) {
            result.add("Property is mandatory", description.getName());
        }

        return result;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((description == null) ? 0 : description.hashCode());
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        result = prime * result + ((value == null) ? 0 : value.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        PropertyConfiguration other = (PropertyConfiguration) obj;
        if (description == null) {
            if (other.description != null)
                return false;
        } else if (!description.equals(other.description))
            return false;
        if (name == null) {
            if (other.name != null)
                return false;
        } else if (!name.equals(other.name))
            return false;
        if (value == null) {
            if (other.value != null)
                return false;
        } else if (!value.equals(other.value))
            return false;
        return true;
    }

    @Override
    public String toString() {
        return "PropertyConfiguration [description=" + description + ", name=" + name + ", value=" + value + "]";
    }

}
